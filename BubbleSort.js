const arr = [5, 8, 1, 3, 4];

function bubbleSort(arr, swap) {
  for (let i = 0; i < arr.length; i++) {
    for (let j = i + 1; j < arr.length; j++) {
      if (arr[i] > arr[j]) {
        let temp = arr[i];
        arr[i] = arr[j];
        arr[j] = temp;
      }
    }
  }
  return arr;
}

document.body.innerHTML = bubbleSort(arr);
